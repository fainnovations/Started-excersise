// Name Cases: Store a person’s name in a variable, and then print that person’s name in lowercase, 
// // uppercase, and titlecase.
var personName1 = function (name) {
    console.log("Lowercase Name: ".concat(name.toLowerCase()), "\nUppercase Name: ".concat(name.toUpperCase()), "\nTitlecase Name: ".concat(name));
};
personName1('Syed Muhammad Faiz');
