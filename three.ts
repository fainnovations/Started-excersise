// Name Cases: Store a person’s name in a variable, and then print that person’s name in lowercase, 
// // uppercase, and titlecase.

let personName1=(name: string)=> {
console.log(`Lowercase Name: ${name.toLowerCase()}`,`\nUppercase Name: ${name.toUpperCase()}`,`\nTitlecase Name: ${name}`);
}
personName1('Syed Muhammad Faiz');